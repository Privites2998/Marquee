/* build-showdown.js — regenerate games/showdown/generated.js from the database.
 *
 * Showdown shows two Broadway shows and a stat; the player picks the bigger
 * value. This builds matchups from data/shows.js + data/stats.js across three
 * numeric axes:
 *   - "Which opened on Broadway LATER?"            (opening year)
 *   - "Which had MORE original-run performances?"  (broadwayPerformances; closed shows only)
 *   - "Which won MORE Tony Awards?"                (tonyWins)
 * Every matchup uses two DIFFERENT shows with DIFFERENT values (no ties), so
 * there is always a correct answer. Output is appended to the 7 curated puzzles
 * in matchups.js, keeping the database as the single source of truth.
 *
 * Deterministic (fixed seed) so re-running produces the same file.
 * Run from the repo root:  node tools/build-showdown.js
 */
'use strict';
const fs = require('fs');
const path = require('path');
const ROOT = path.resolve(__dirname, '..');
function load(file, prop) {
  const win = {};
  new Function('window', fs.readFileSync(path.join(ROOT, file), 'utf8'))(win);
  return win[prop];
}
const shows = load('data/shows.js', 'MarqueeShows');
const stats = load('data/stats.js', 'MarqueeStats');

// reproducible PRNG (mulberry32)
let seed = 0x9e3779b9;
function rnd() {
  seed |= 0; seed = (seed + 0x6D2B79F5) | 0;
  let t = Math.imul(seed ^ (seed >>> 15), 1 | seed);
  t = (t + Math.imul(t ^ (t >>> 7), 61 | t)) ^ t;
  return ((t ^ (t >>> 14)) >>> 0) / 4294967296;
}
const pick = a => a[Math.floor(rnd() * a.length)];
const shuffle = a => { a = a.slice(); for (let i = a.length - 1; i > 0; i--) { const j = Math.floor(rnd() * (i + 1)); [a[i], a[j]] = [a[j], a[i]]; } return a; };
const yearOf = s => parseInt(s.broadwayOpen.slice(0, 4), 10);
const comma = n => n.toLocaleString('en-US');

const pools = {
  year: shows.map(s => ({ name: s.title, value: yearOf(s) })),
  perf: shows.filter(s => s.status === 'closed' && stats[s.id] && stats[s.id].broadwayPerformances != null)
             .map(s => ({ name: s.title, value: stats[s.id].broadwayPerformances })),
  tony: shows.filter(s => s.tonyWins != null).map(s => ({ name: s.title, value: s.tonyWins }))
};
const META = {
  year: { prompt: 'Which opened on Broadway LATER?',           label: 'opened',       fmt: v => '' + v },
  perf: { prompt: 'Which had MORE original-run Broadway performances?', label: 'performances', fmt: comma },
  tony: { prompt: 'Which won MORE Tony Awards?',               label: 'Tony wins',    fmt: v => '' + v }
};

function makeMatchup(type, usedKeys) {
  const pool = pools[type], m = META[type];
  for (let tries = 0; tries < 200; tries++) {
    const a = pick(pool), b = pick(pool);
    if (a.name === b.name || a.value === b.value) continue;
    const key = type + '|' + [a.name, b.name].sort().join('|');
    if (usedKeys.has(key)) continue;
    usedKeys.add(key);
    return {
      _key: key,
      prompt: m.prompt,
      a: { name: a.name, value: a.value, label: m.label },
      b: { name: b.name, value: b.value, label: m.label },
      note: a.name + ': ' + m.fmt(a.value) + '.  ' + b.name + ': ' + m.fmt(b.value) + '.'
    };
  }
  return null;
}

const NUM = 43;                          // 43 generated + 7 curated = 50 days
const slotTypes = ['perf', 'perf', 'tony', 'tony', 'year']; // 5 matchups per puzzle
const usedKeys = new Set();
const puzzles = [];
let id = 8, guard = 0;
while (puzzles.length < NUM && guard++ < 20000) {
  const matchups = [];
  const localKeys = new Set();
  for (const t of shuffle(slotTypes)) {
    let made = null;
    for (let tries = 0; tries < 60; tries++) {
      const cand = makeMatchup(t, usedKeys);
      if (cand && !localKeys.has(cand._key)) { made = cand; break; }
    }
    if (!made) break;
    localKeys.add(made._key);
    delete made._key;
    matchups.push(made);
  }
  if (matchups.length === 5) puzzles.push({ id: id++, matchups });
}

const line = m =>
  '      { prompt: ' + JSON.stringify(m.prompt) +
  ', a: { name: ' + JSON.stringify(m.a.name) + ', value: ' + m.a.value + ', label: ' + JSON.stringify(m.a.label) + ' }' +
  ', b: { name: ' + JSON.stringify(m.b.name) + ', value: ' + m.b.value + ', label: ' + JSON.stringify(m.b.label) + ' }' +
  ', note: ' + JSON.stringify(m.note) + ' }';
const body = puzzles.map(p =>
  '  {\n    id: ' + p.id + ',\n    matchups: [\n' + p.matchups.map(line).join(',\n') + '\n    ]\n  }'
).join(',\n');

const out =
`/* AUTO-GENERATED by tools/build-showdown.js from data/shows.js + data/stats.js.
   Do not edit by hand. ${puzzles.length} database-derived matchup puzzles
   (opening year, original-run performances, Tony wins) appended to the 7
   curated puzzles in matchups.js. Re-run the generator after the database changes. */
(function () {
  var GEN = [
${body}
  ];
  window.ShowdownPuzzles = (window.ShowdownPuzzles || []).concat(GEN);
})();
`;
fs.writeFileSync(path.join(ROOT, 'games', 'showdown', 'generated.js'), out, 'utf8');
console.log('Wrote games/showdown/generated.js with ' + puzzles.length + ' puzzles (' + (puzzles.length * 5) + ' matchups).');
console.log('Pools: year ' + pools.year.length + ', perf ' + pools.perf.length + ', tony ' + pools.tony.length + '.');
